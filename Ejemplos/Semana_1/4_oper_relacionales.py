a = "Hola"
b = "HOLA"

print("Igualdad: {}".format(a == b))
print("Igualdad: {}".format(a == "Hola"))
print("-"*30)

print("Distinto: {}".format(a != b))
print("Distinto: {}".format(a != "Hola"))
print("-"*30)

num1 = 10
num2 = 20

print("Valor num1: {}".format(num1))
print("Valor num2: {}\n".format(num2))
print("Mayor: {}".format( num1 > num2))
print("Menor: {}".format( num1 < num2))
print("Mayor igual: {}".format( num1 >= 10))
print("Menor igual: {}".format( num2 <= 20))
